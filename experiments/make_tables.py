"""Generate LaTeX tables for the manuscript directly from experiment JSON.

Writes self-contained ``tabular`` blocks into manuscript/tables/, which the
manuscript \\input{}s. This removes hand-transcription: re-running the
experiment and this script updates the numbers in the paper automatically.
"""
import json
from pathlib import Path

ROOT = Path(__file__).parent.parent
RESULTS = ROOT / "results"
TABLES = ROOT / "results" / "tables"
TABLES.mkdir(parents=True, exist_ok=True)


def fmt_mse(v):
    # Magnitude-adaptive precision: the natural-norm sweep spans MSE from ~0.004 to ~33.
    if v >= 100:
        return f"{round(v):,}".replace(",", "{,}")  # LaTeX thin thousands separator
    if v >= 10:
        return f"{v:.1f}"
    if v >= 1:
        return f"{v:.2f}"
    if v >= 0.01:
        return f"{v:.3f}"
    return f"{v:.4f}"


def fmt(v, prec):
    s = f"{v:.{prec}f}"
    return "0." + "0" * prec if s == "-" + "0." + "0" * prec else s  # kill -0.000


def perceptual_table():
    """Exp14: SSIM/LPIPS (+ MSE/PSNR/coverage) on natural-norm data."""
    rows = json.load(open(RESULTS / "exp14" / "combined_table.json"))
    # preserve dataset order of first appearance, sort sigma within
    order = []
    for r in rows:
        if r["dataset"] not in order:
            order.append(r["dataset"])

    lines = [
        r"\begin{tabular}{lrrrrrr}",
        r"\toprule",
        r"Dataset & $\sigma$ & MSE & PSNR & SSIM & LPIPS & Cov.\ (\%) \\",
        r"\midrule",
    ]
    for di, ds in enumerate(order):
        drows = sorted([r for r in rows if r["dataset"] == ds], key=lambda r: r["sigma"])
        short = ds.split(" (")[0]
        for i, r in enumerate(drows):
            first = (rf"\multirow{{{len(drows)}}}{{*}}{{{short}}}" if i == 0 else "")
            lines.append(
                f"{first:<28} & ${r['sigma']:g}$ & ${fmt_mse(r['mse_mean'])}$ "
                f"& ${fmt(r['psnr_mean'], 1)}$ & ${fmt(r['ssim_mean'], 3)}$ "
                f"& ${fmt(r['lpips_mean'], 2)}$ & ${fmt(r['coverage_95'] * 100, 1)}$ \\\\"
            )
        if di < len(order) - 1:
            lines.append(r"\midrule")
    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")

    out = TABLES / "perceptual_table.tex"
    out.write_text("% AUTO-GENERATED by experiments/make_tables.py -- do not edit by hand.\n"
                   + "\n".join(lines) + "\n")
    print(f"Wrote {out}")


if __name__ == "__main__":
    perceptual_table()
